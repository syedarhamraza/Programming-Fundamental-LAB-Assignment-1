//Task 4: Basic Currency Conversion 
#include <iostream>
using namespace std;

int main() {
    int USD = 100;
    float EUR = USD * 0.85;
    int JPY = USD * 110;

    cout << "Amount in USD is: " << USD << endl;
    cout << "Amount in EUR is: " << EUR << endl;
    cout << "Amount in JPY is: " << JPY << endl;

    return 0;
}